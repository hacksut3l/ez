     <h2><span style="color: rgb(0, 153, 153);">What happens if I have a dispute with a Funeral Director?</span></h2>
<p class="MsoNormal">Unfortunately, like all industries, this can happen from time to time. As with all consumer transactions, the first thing to do if you have a dispute with a funeral service provider is to clearly explain your problem to them and to ask what they can do to rectify the matter.</p>
<p class="MsoNormal">Disputes can be the result of misunderstanding so it is important to remain calm but firm.</p>
<p class="MsoNormal">You might also seek assistance from whichever industry association the funeral director belongs to &ndash; look for the name on any paperwork you have received. These associations usually have a system for reporting.</p>
<p class="MsoNormal">If you exhaust these avenues you can contact the Office of Fair Trading or you may lodge a formal complaint if approaches to the funeral service provider have been unsuccessful.</p>
<p class="MsoNormal">The important thing to remember is to keep all your paperwork and receipts, as well as records of everyone you speak to, dates and what were discussed.<br /><br /></p>

<h2>Funeral Director Associations</h2>
<p class="MsoNormal">Most funeral directors are members of an association and are bound by a code of ethics and a minimum set of standards must be maintained at all times. The peak associations for the funeral industry in Australia are:<br /><br /></p>

<h4><span style="color: rgb(0, 153, 153);"><span style="font-size: larger;">Australian Funeral Directors Association (AFDA)</span></span></h4>
<p class="MsoNormal">The AFDA is a national association of funeral directors, with members in every state and territory. It will take your complaint if it is about one of its members.<br />
