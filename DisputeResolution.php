<h3><span style="font-size: medium;">Are all funeral directors members of a Funeral Directors Association?</span></h3>
<p class="MsoNormal">No. Not all funeral directors are members of a Funeral Directors Association and they are under no obligation to adhere to the association's policy and guidelines.<br /><br /></p>

