Fax - (03) 9819 7390<br /><br /></p>

<h4><span style="color: rgb(0, 153, 153);"><span style="font-size: larger;">Independent Funeral Directors Association Australia (IFDAA)</span></span></h4>
<p class="MsoNormal">The Independent Funeral Directors Assoc Australia Incorporated was formed some 10 years ago. The IFDAA says its members work together to ensure that the families they serve receive the best possible standard of service at the most realistic cost. IFDAA has members in most Australian states.<br />
